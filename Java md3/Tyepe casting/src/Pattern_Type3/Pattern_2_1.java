package Pattern_Type3;

public class Pattern_2_1 {

	public static void main(String[] args) {
     for(int row=1;row<=4;row++) {
    	 for(int col=1;col<=3;col++) {
    		 if(col==1)
    			 System.out.print("A");
    		 if(col==2)
    			 System.out.print("B");
    		 if(col==3)
    			 System.out.print("C");
    	 }
    	 System.out.println();
     }
	}

}
